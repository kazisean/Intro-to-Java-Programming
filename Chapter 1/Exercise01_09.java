/*
*           Description : (Area and perimeter of a rectangle) Write a program that displays the area and perimeter of a rectangle with the width of 4.5 and height of 7.9 using the following formula:
*                           Area = width * height
*/

public class Exercise01_09 {
    public static void main (String [] args){
        System.out.println("Area = " + 4.5 * 7.9);
        System.out.println("Perimeter = " + 2.0 * (4.5 + 7.9));
    }
}